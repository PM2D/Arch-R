    log "  Timings: ${CLONE_HFP[$panel_name]}/${CLONE_HSYNC[$panel_name]}/${CLONE_HBP[$panel_name]} ${CLONE_VFP[$panel_name]}/${CLONE_VSYNC[$panel_name]}/${CLONE_VBP[$panel_name]}"

