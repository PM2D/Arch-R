declare -A PANEL_VBP=(
    [0]=13   [1]=12   [2]=14  [3]=14  [4]=13   [5]=13
)
declare -A PANEL_PIXCLK=(
    [0]=1  [1]=1  [2]=1  [3]=1  [4]=1  [5]=0
)
declare -A PANEL_RESET_DELAY=(
    [0]=1    [1]=150  [2]=150  [3]=150  [4]=150  [5]=50
)
declare -A PANEL_PREPARE_DELAY=(
